print(f"精细结构常数 α = {alpha_em:.6f}")

# 黄金比例相关的数学常数
phi = (1 + np.sqrt(5)) / 2
print(f"黄金比例 φ = {phi:.6f}")

def fibonacci(n):
    if n <= 0: return 0
    elif n == 1: return 1
    else:
        a, b = 0, 1
        for _ in range(2, n + 1):
            a, b = b, a + b
        return b

print("\n=== 理论声称与现实的对比 ===")

attempts = [
    ("φ^(-7)", phi**(-7)),
    ("1/(F₇×φ)", 1/(fibonacci(7) * phi)),
    ("1/φ²", 1/phi**2),
    ("1/(2φ²)", 1/(2*phi**2)),
    ("log(φ)/φ²", np.log(phi)/phi**2),
    ("1/(F₅×φ²)", 1/(fibonacci(5) * phi**2)),
    ("1/(F₈)", 1/fibonacci(8)),
]

